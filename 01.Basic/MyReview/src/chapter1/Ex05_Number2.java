package chapter1;

public class Ex05_Number2 {

	public static void main(String[] args) {
		int array[][] = {
				{10, 20, 30},
				{40, 50},
				{60, 70, 80, 90},
		};
		
		System.out.println("일차원 배열의 요소수" + array.length);
		System.out.println("array[0]의 요소수 = " + array[0].length);
		System.out.println("array[1]의 요소수 = " + array[1].length);
		System.out.println("array[2]의 요소수 = " + array[2].length);
		
	}
}
